import string

def print_rangoli(size):
    alphabet = list(string.ascii_lowercase)
    alphabet = alphabet[:size][::-1]
    chunk = [alphabet[:size-num]+alphabet[:len(alphabet)-1-num][::-1] for num in range(size)]
    down_result = ["-" * item * 2 + "-".join(chunk[item]) + "-" * item * 2 for item in range(len(chunk))]
    up_result = down_result.copy()
    up_result.reverse()
    for item in up_result:
        print(item)
    for idx in range(len(down_result)):
        if idx == 0:
            continue
        else:
            print(down_result[idx])

if __name__ == '__main__':
    n = 5
    print_rangoli(n)
